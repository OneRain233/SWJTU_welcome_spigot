package com.swjtu.welcome;

public class setColor {
    public String setYellow(String str) {
        return "§6§l" + str;
    }

    public String setRed(String str) {
        return "§c§l" + str;
    }

    public String setGreen(String str) {
        return "§a§l" + str;
    }

    public String setBlue(String str) {
        return "§9§l" + str;
    }

    public String setWhite(String str) {
        return "§f§l" + str;
    }

    public String setBlack(String str) {
        return "§0§l" + str;
    }
}
